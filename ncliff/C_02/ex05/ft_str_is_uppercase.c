int		ft_str_is_uppercase(char *str)
{
	int b;

	b = 0;
	while (*str != '\0')
	{
		if (!(65 <= *str && *str <= 90))
		{
			b++;
		}
		str++;
	}
	if (b > 0)
	{
		return (0);
	}
	else
	{
		return (1);
	}
}
