int		ft_str_is_alpha(char *str)
{
	int b;
	int c;

	b = 0;
	c = 0;
	while (*str != '\0')
	{
		if ((65 <= *str && *str <= 90) || (97 <= *str && *str <= 122))
		{
			b++;
		}
		else
		{
			c++;
		}
		str++;
	}
	if (c > 0)
	{
		return (0);
	}
	else
	{
		return (1);
	}
}
