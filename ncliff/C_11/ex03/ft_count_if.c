int ft_count_if(char **tab, int length, int(*f)(char*))
{
	int i;
	int n;
	int pnt;

	i = 0;
	n = 0;
	pnt = 0;
	while (i < length)
	{
		n = 0;
		if (f(tab[i]) != 0)
		{
			pnt++;
		}
		i++;
	}
	return (pnt);
}
