#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		n;
	char	*tab;

	n = 0;
	while (src[n] != '\0')
	{
		n++;
	}
	tab = malloc(n);
	n = 0;
	while (src[n] != '\0')
	{
		tab[n] = src[n];
		n++;
	}
	return (tab);
}
