int		ft_is_prime(int nb)
{
	int flag;

	if (nb < 2)
		return (0);
	flag = 2;
	while (flag <= nb / 2)
	{
		if (nb % flag == 0)
			return (0);
		flag++;
	}
	return (1);
}
