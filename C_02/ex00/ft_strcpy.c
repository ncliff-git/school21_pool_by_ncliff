char		*ft_strcpy(char *dest, char *src)
{
	int sum;

	sum = 0;
	while (*(src + sum) != '\0')
	{
		*(dest + sum) = *(src + sum);
		sum++;
	}
	*(dest + sum) = '\0';
	return (dest);
}
