int		ft_str_is_numeric(char *str)
{
	int b;

	b = 0;
	while (*str != '\0')
	{
		if (!(48 <= *str && *str <= 58))
		{
			b++;
		}
		str++;
	}
	if (b > 0)
	{
		return (0);
	}
	else
	{
		return (1);
	}
}
