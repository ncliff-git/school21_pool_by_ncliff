#include <unistd.h>

void		ft_is_negative(int n)
{
	char n1;
	char p1;

	n1 = 'N';
	p1 = 'P';
	if (n >= 0)
	{
		write(1, &p1, 1);
	}
	else
	{
		write(1, &n1, 1);
	}
}
